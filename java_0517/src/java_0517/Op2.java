package java_0517;

public class Op2 {
   public static void main(String[] args) {
	   // 증감 연산자 => ++ , --
	  
	   int x=10;
	   System.out.println(x);
	   
	   x++; // 변수의 값을 1 증가시킨다.
	   System.out.println(x);
	   
	   // --
	   int y=17;
	   System.out.println(y);
	   
	   y--;
	   System.out.println(y);
  } // main()
} // class
