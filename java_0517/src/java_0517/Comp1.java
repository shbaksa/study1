package java_0517;

public class Comp1 {

	public static void main(String[] args) {
		//  비교연산자 => 2개의 요소를 비교하는 연산자 => 결과는 true, false로 가져온다.
		
		System.out.println(10 > 20); // 10이 20보다 크다
        System.out.println(10 < 20); // 10이 20보다 작다 , 20이 10보다 크다
        System.out.println(10 == 20); // 같다
        
        System.out.println(10 >= 20); // 크거나 같다
        System.out.println(10 <= 20); // 작거나 같다
        
        System.out.println(10 != 20); // 같지 않다
      
	}

}
