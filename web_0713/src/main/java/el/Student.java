package el;

public class Student {
    private String name;
    private int age;
    private String phone;
    
    // private인 속성에 입력과 출력을 위해 필요한거
    // getter, setter
    
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
    
    
    
    
}
