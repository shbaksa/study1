package m_0517;

public class V3 {

	public static void main(String[] args) {
		// System.out.println()에 1개 이상의 값을 출력하기

		System.out.println("안녕");
		System.out.println("하세요");
		
		// 안녕 + 하세요 
		System.out.println("안녕"+"하세요");
		
		// 숫자+숫자
		System.out.println(99+88);
		// 숫자+문자
		System.out.println(99+"오늘");
		// 문자+문자
		System.out.println("오늘"+"은"+"화요일");
 	}

}
